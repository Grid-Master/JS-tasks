// </tbody>
// </table>
// В ней может быть больше строк.

// Напишите код для сортировки по столбцу "name".

let trs = Array.from(document.querySelectorAll('tbody > tr'));

trs.sort((rowA, rowB) => {
  return rowA.querySelector('td:first-child').innerHTML >
    rowB.querySelector('td:first-child').innerHTML
    ? 1
    : -1;
});

let tbody = document.querySelector('tbody');
tbody.append(...trs);
